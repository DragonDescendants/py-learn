import torch as t

a = t.arange(0,27).view(3,3,3) # 三维矩阵
print('a:',a,'\n',
      'no1:',a[[1,2],[1,1],[2,1]],'\n',  # 取(1,1,2)和(2,1,1)两个元素
      'no2:',a[[2,1,0],[1],[0]],'\n', # 取(2,1,0) (1,1,0) (0,1,0) 三个元素
      'no3:',a[[0,2],...,0:2],'\n', # 取a[0] a[2]中的0,1列元素 
      ) 

